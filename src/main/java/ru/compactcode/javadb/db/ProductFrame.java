    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Считываем данные из полей, создаем объект и добавляем его в БД Product

        try {
            int id = -1;
            String idText = jTextField1.getText().trim();
            if (!idText.isEmpty()) {
                try {
                    id = Integer.parseInt(idText);
                    if (id <= 0) {
                        // можно бросить исключение или установить id = -1; здесь выбрасываем.
                        throw new NumberFormatException("ID must be positive");
                    }
                } catch (NumberFormatException nfe) {
                    // Обработка: печать стека пока, можно показать диалог
                    nfe.printStackTrace();
                    return;
                }
            }

            String name = jTextField2.getText().trim();
            String desc = jTextField3.getText().trim();
            double price = Double.parseDouble(jTextField4.getText().trim());
            int stock = Integer.parseInt(jTextField5.getText().trim());

            Product p;
            if (id > 0) {
                p = new Product(id, name, desc, price, stock);
            } else {
                p = new Product(name, desc, price, stock);
            }

            db.insertProduct(p);
            loadData();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /* Получаем выбранную строку, считываем данные из полей, 
        *создаем объект с существующим ID и обновляем запись в БД
         */

        int row = jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }

        try {
            int id = (int) model.getValueAt(row, 0);

            String name = jTextField2.getText();
            String desc = jTextField3.getText();
            double price = Double.parseDouble(jTextField4.getText());
            int stock = Integer.parseInt(jTextField5.getText());

            Product p = new Product(id, name, desc, price, stock);

            db.updateProduct(p);
            loadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Получаем выбранную строку и удаляем продукт с соответствующим ID из БД

        int row = jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }

        try {
            int id = (int) model.getValueAt(row, 0);
            db.deleteProductById(id);
            loadData();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

