    private void loadData() {
        try {
            model.setRowCount(0);

            for (Product p : db.getAllProducts()) {
                model.addRow(new Object[]{
                    p.getId(),
                    p.getName(),
                    p.getDescription(),
                    p.getPrice(),
                    p.getStock()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

